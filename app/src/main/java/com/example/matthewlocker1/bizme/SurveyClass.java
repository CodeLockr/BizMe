public class SurveyClass {

    String yes;
    String no;
    String sunday;
    String tuesday;
    String favorite;
    String email;
    String company;

    public SurveyClass () {

    }

    public SurveyClass (String yes, String no, String sunday, String tuesday, String favorite, String email, String company) {
        this.yes = yes;
        this.no = no;
        this.sunday = sunday;
        this.tuesday = tuesday;
        this.favorite = favorite;
        this.email = email;
        this.company = company;
    }


}
